//----------------------------------------------------------------------------------------------
// Class Section
//----------------------------------------------------------------------------------------------
export class EnvironmentService
{
    //------------------------------------------------------------------------------------------
    // Static Methods Section
    //------------------------------------------------------------------------------------------
    public static getInstance(): EnvironmentService
    {
        if (EnvironmentService._instance == null)
        {
            EnvironmentService._instance = new EnvironmentService();
        }
        return EnvironmentService._instance;
    }

    //------------------------------------------------------------------------------------------
    // Static Fields Section
    //------------------------------------------------------------------------------------------
    private static _instance: EnvironmentService;

    //------------------------------------------------------------------------------------------
    // Public Fields Section
    //------------------------------------------------------------------------------------------
    private validationApiURL: string
    
    
    
    //------------------------------------------------------------------------------------------
    // Constructor Method Section
    //------------------------------------------------------------------------------------------
    constructor() {
        this.validationApiURL = 'http://localhost:4000/api/validations';
    }


    //------------------------------------------------------------------------------------------
    // Public Methods Section
    //------------------------------------------------------------------------------------------
    public getApiUrlFor(schema: string): string
    {
        return `${this.validationApiURL}/${schema}`;
    }

}
